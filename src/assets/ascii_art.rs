pub fn get(of: &str) -> (&'static str, [Option<&'static str>; 4]) {
    for art in ASCII_ART.iter() {
        let is_alias = art.3.starts_with("@");
        let get_tuple = || -> (&'static str, [Option<&'static str>; 4]) {
            if !is_alias {
                (
                    art.3,
                    art.4
                        .unwrap_or([Some("\u{001b}[38;5;7m"), None, None, None]),
                )
            } else {
                get_cli(&art.3[1..])
            }
        };
        match art.1 {
            Check::Is => {
                if art.2.is_none() {
                    if of.to_lowercase() == art.0.to_lowercase() {
                        return get_tuple();
                    }
                } else {
                    if of.to_lowercase() == art.2.clone().unwrap().to_lowercase() {
                        return get_tuple();
                    }
                }
            }
            Check::Contains => {
                if art.2.is_none() {
                    if of.to_lowercase().contains(&art.0.to_lowercase()) {
                        return get_tuple();
                    }
                } else {
                    if of.contains(&art.2.clone().unwrap()) {
                        return get_tuple();
                    }
                }
            }
            Check::StartsWith => {
                if art.2.is_none() {
                    if of.to_lowercase().starts_with(&art.0.to_lowercase()) {
                        return get_tuple();
                    }
                } else {
                    if of
                        .to_lowercase()
                        .starts_with(&art.2.clone().unwrap().to_lowercase())
                    {
                        return get_tuple();
                    }
                }
            }
        }
    }
    return get_cli("linux");
}

pub fn get_cli(of: &str) -> (&'static str, [Option<&'static str>; 4]) {
    for art in ASCII_ART.iter() {
        if art.0 == of {
            return (
                art.3,
                art.4
                    .unwrap_or([Some("\u{001b}[38;5;7m"), None, None, None]),
            );
        }
    }
    return get_cli("linux");
}
